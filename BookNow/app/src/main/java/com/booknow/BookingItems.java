package com.example.nick.booknow;

public class BookingItems {
    public String missionName;
    public String dateBooking;
    public String hourBooking;
    public String numDiners;

    public BookingItems(String missionName, String dateBooking, String hourBooking, String numDiners){
        this.missionName = missionName;
        this.dateBooking = dateBooking;
        this.hourBooking = hourBooking;
        this.numDiners = numDiners;
    }

}
